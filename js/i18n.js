// Current language
let currentLanguage = localStorage.getItem('language') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Set HTML language attribute
    document.documentElement.lang = currentLanguage;
    // Set text direction for Arabic
    document.documentElement.dir = (currentLanguage === 'ar') ? 'rtl' : 'ltr';
    
    // Update language selector
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = currentLanguage;
    }
    
    // Translate all elements
    translatePage();
});

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = (lang === 'ar') ? 'rtl' : 'ltr';
    translatePage();
}

// Translate page content
function translatePage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key);
        // If element has a data-i18n-html attribute, set innerHTML
        if (element.hasAttribute('data-i18n-html')) {
            element.innerHTML = translation;
        } else {
            element.textContent = translation;
        }
    });

    // Translate placeholders and titles
    const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
    placeholders.forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.placeholder = getTranslation(key);
    });

    const titles = document.querySelectorAll('[data-i18n-title]');
    titles.forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.title = getTranslation(key);
    });

    // Translate option elements
    const options = document.querySelectorAll('option[data-i18n]');
    options.forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = getTranslation(key);
    });

    // If a nav translation helper exists (defined in main.js), call it so
    // navigation links update immediately when language changes.
    if (typeof translateNavLinks === 'function') {
        try {
            translateNavLinks();
        } catch (e) {
            console.error('translateNavLinks error:', e);
        }
    }

    // Set document title based on filename mapping
    const file = window.location.pathname.split('/').pop() || 'index.html';
    const titleMap = {
        'index.html': 'home.welcome',
        'students.html': 'students.title',
        'faculty.html': 'faculty.title',
        'schedule.html': 'schedule.title',
        'timetable.html': 'timetable.title',
        'about.html': 'about.title',
        'contact.html': 'contact.title',
        'admission.html': 'admission.title',
        'login.html': 'login.title',
        'student-dashboard.html': 'dashboard.student',
        'professor-dashboard.html': 'dashboard.professor',
        'dean-dashboard.html': 'dashboard.dean'
    };

    if (titleMap[file]) {
        document.title = getTranslation(titleMap[file]);
    }
}

// Get translation for a key
function getTranslation(key) {
    if (translations[currentLanguage] && translations[currentLanguage][key]) {
        return translations[currentLanguage][key];
    }
    // Fallback to English
    if (translations['en'] && translations['en'][key]) {
        return translations['en'][key];
    }
    return key;
}
